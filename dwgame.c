/* рисует горизонтальную линию */
static void HLine(int x1, int x2, int y, int color)
{
	int length, i;
	UINT pixel,*v1,*v2;

	/* сделаем чтобы x2 > x1 */
	if (x1 > x2) {
		unsigned int temp = x1;
		x1 = x2;
		x2 = temp;
	}

	/* расчитаем цвет линии */
	pixel = palette[color];
	/* расчитаем длину линии */
	length = x2 - x1 + (1 * 2);
	/* нарисуем линию */
	v1 = ptImage + SCREEN_WIDTH * y + x1;
	v2 = v1 + SCREEN_WIDTH;
	for (i = 0; i < length; i++) {
		v1[i] = pixel;
		v2[i] = pixel;
	}
}

/* рисует вертикальную линию */
static void VLine(int y1, int y2, int x, int color)
{
	int i,length;
	UINT pixel,*v1,*v2;

	/* сделаем чтобы y2 > y1 */
	if (y1 > y2) {
		int temp = y1;
		y1 = y2;
		y2 = temp;
	}

	/* расчитаем служебные переменные */
	v1 = ptImage + SCREEN_WIDTH * y1 + x;
	v2 = v1 + 1;
	length = y2 - y1 + 1;
	pixel = palette[color];
	/* нарисуем линию */
	for (i = 0; i <= length; i++) {
		/* отобразим точки */
		*v1 = pixel;
		*v2 = pixel;
		/* увеличим координаты */
		v1 += SCREEN_WIDTH;
		v2 += SCREEN_WIDTH;
	}
}

