/* отобразить ракетку */
static void ShapeRacket(void)
{
	static int p = 0, add = 1;
	const UCHAR *pRacket;
	int x,y,p1,p2,width,limitPoint;

	/* проверим, следует отображать ракетку или нет */
	if (!scene.pRacket.show)
		return;

	/* определим лимиты дл€ точек */
	switch (scene.pRacket.type) {
		case TYPE_RACKET_NORMAL: limitPoint = 3; break;
		case TYPE_RACKET_GUN: limitPoint = 3; break;
		case TYPE_RACKET_EXTEND: limitPoint = 11; break;
		case TYPE_RACKET_N_G_1: limitPoint = 3; break;
		case TYPE_RACKET_N_G_2: limitPoint = 3; break;
		case TYPE_RACKET_N_G_3: limitPoint = 3; break;
		case TYPE_RACKET_N_G_4: limitPoint = 3; break;
		case TYPE_RACKET_N_E_1: limitPoint = 4; break;
		case TYPE_RACKET_N_E_2: limitPoint = 5; break;
		case TYPE_RACKET_N_E_3: limitPoint = 6; break;
		case TYPE_RACKET_N_E_4: limitPoint = 7; break;
		case TYPE_RACKET_N_E_5: limitPoint = 8; break;
		case TYPE_RACKET_N_E_6: limitPoint = 9; break;
		case TYPE_RACKET_N_E_7: limitPoint = 10; break;
		default: return;
	}

	/* определим отображаемую ракетку */
	pRacket = GetRacketType(scene.pRacket.type,&width);
	/* расчитаем позицию ракетки */
	x = GetX(scene.pRacket.X);
	y = GetY(scene.pRacket.Y);
	/* отобразим рисунок ракетки */
	PlotSurface(pRacket,width,width,13,x,y);

	/* расчитаем логические позиции точек */
	p += add;
	/* проверим выход за границы */
	if (add > 0) {
		if (p > limitPoint) {
			p = limitPoint;
			add = -1;
		}
	} else {
		if (p < 0) {
			p = 0;
			add = 1;
		}
	}

	/* расчитаем позиции точек в ракетке */
	y += 12;
	p1 = x + ((width - 4) / 2 - 1 - p) * SCALE;
	p2 = x + ((width - 4) / 2 + p) * SCALE;
	/* отобразим точки */
	PlotPixel(p1,y,CL_BLACK);
	PlotPixel(p2,y,CL_BLACK);
}

