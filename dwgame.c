/* отображает сообщение с учетом прозрачности */
static void PlotMessage(const UCHAR *pattern, int pitchPattern,
						int width, int height, int x, int y)
{
	UINT *video,color;
	int i,j;

	/* расчитаем точку от которой начинаетс€ прорисовка поверхности */
	video = ptImage + y * SCREEN_WIDTH + x;
	color = palette[CL_WHITE];

	/* отобразим поверхность */
	for (i = 0; i < height; i++) {
		for (j = 0; j < width; j++) {
			UINT *p;
			/* проверим пиксель на прозрачность */
			if (pattern[j])
				continue;
			/* отобразим пиксель белым цветом */
			p = video + (j * SCALE);
			*(p) = color;
			*(p + 1) = color;
			*(p + SCREEN_WIDTH) = color;
			*(p + SCREEN_WIDTH + 1) = color;
		}
		/* переместимс€ на следующие строчки поверхностей */
		video += SCREEN_WIDTH * 2;
		pattern += pitchPattern;
	}
}

/*
 * ќтображает сообщение.
 * –азмер сообщени€: 160,68
 *  оордината верхней левой точки: 88,132
 */
static void ShapeMessage(void)
{
	const UCHAR *p;
	UINT *v,pixel;
	int i,j,x,y,X,Y;

	/* отобразить сообщение */
	if (!scene.message.show)
		return;

	/* расчитаем оконные координаты */
	X = GetX(88);
	Y = GetY(132);

	/* очистим поле где отображаетс€ сообщение */
	v = ptImage + Y * SCREEN_WIDTH + X;
	pixel = palette[CL_SCREEN_BACK];
	for (i = 0; i < 68; i++, v += SCREEN_WIDTH) {
		for (j = 0; j < 160; j++)
			v[j] = pixel;
	}

	/* отобразим сообщение */
	if (scene.message.flags == MESSAGE_ROUND) {
		TCHAR buff[32];
		/* строка 'player' */
		x = X + 8 * SCALE;
		y = Y + 6 * SCALE;
		PlotMessage(pPlayer,47,47,10,x,y);
		/* номер игрока */
		p = pDigitMsg + scene.message.player * 6;
		x = X + (8 * 8 + 1) * SCALE;
		y = Y + (6 * SCALE);
		PlotMessage(p,60,6,8,x,y);
		/* строка 'round' */
		x = X + (8 + 1) * SCALE;
		y = Y + (6 + 8 + 6) * SCALE;
		PlotMessage(pRound,39,39,8,x,y);
		/* номер раунда */
		_stprintf(buff,_T("%02d"),scene.message.round);
		for (i = 0; i < 2; i++) {
			p = pDigitMsg + (buff[i] - _T('0')) * 6;
			x = X + (i * 8 + 7 * 8 + 1) * SCALE;
			y = Y + (6 + 8 + 6) * SCALE;
			PlotMessage(p,60,6,8,x,y);
		}
	} else {
		/* информаци€ об паузе */
		if (scene.message.pause) {
			/* отобразим цифру */
			p = pDigitMsg + scene.message.pause * 6;
			x = X + 37 * SCALE;
			y = Y + 13 * SCALE;
			PlotMessage(p,60,6,8,x,y);
		} else {
			x = X + 20 * SCALE;
			y = Y + 15 * SCALE;
			PlotMessage(pPause,39,39,8,x,y);
		}
	}
}

